package linkedList;

public class LinkedList {

  public class Node {

    public int value;
    public int next;

    public Node(int number) {
      this.value = number;
      this.next = next;
    }
  }

    public Node head;
    public Node tail;

    public LinkedList() {
      this.head = null;
      this.tail = null;
    }

  }
